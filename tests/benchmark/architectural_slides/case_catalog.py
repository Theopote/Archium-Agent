@dataclass(frozen=True)
class CaseAssetSpec:
    """One deterministic placeholder asset for a benchmark case."""

    asset_id: UUID
    visual_type: VisualType
    description: str
    placeholder_label: str


@dataclass(frozen=True)
class CaseCatalogEntry:
    """Full specification for one benchmark case."""

    definition: BenchmarkCaseDefinition
    slide_title: str
    message: str
    key_points: tuple[str, ...] = ()
    metrics: tuple[str, ...] = ()
    captions: tuple[str, ...] = ()
    insight: str | None = None
    assets: tuple[CaseAssetSpec, ...] = ()
    hero_asset_id: UUID | None = None
    supporting_asset_ids: tuple[UUID, ...] = ()
    dominant_content_type: VisualContentType | None = None
    preferred_layout_families: tuple[LayoutFamily, ...] = ()
    drawing_hero: bool = False
    source_document: str = "项目资料.pdf"
    source_page: int = 1


def catalog_asset_uuid(case_number: int, asset_index: int) -> UUID:
    """Deterministic asset UUID for cases 006+ (index starts at 1)."""
    return UUID(
        f"c{case_number:03d}{asset_index:04d}-0001-4001-8001-{asset_index:012d}"
    )


def _asset(
    case_number: int,
    asset_index: int,
    visual_type: VisualType,
    description: str,
    *,
    asset_id: UUID | None = None,
) -> CaseAssetSpec:
    return CaseAssetSpec(
        asset_id=asset_id or catalog_asset_uuid(case_number, asset_index),
        visual_type=visual_type,
        description=description,
        placeholder_label=description,
    )


def _definition(
    case_id: str,
    *,
    title: str,
    category: ArchitecturalSlideCategory,
    page_type: str,
    page_task: str,
    visual_focus: str,
    expected_layout_family: LayoutFamily,
    allowed_layout_variants: tuple[str, ...],
    layout_variant: str,
    chapter_id: str,
    slide_order: int,
) -> BenchmarkCaseDefinition:
    return BenchmarkCaseDefinition(
        case_id=case_id,
        title=title,
        category=category,
        page_type=page_type,
        page_task=page_task,
        visual_focus=visual_focus,
        expected_layout_family=expected_layout_family,
        allowed_layout_variants=list(allowed_layout_variants),
        layout_variant=layout_variant,
        chapter_id=chapter_id,
        slide_order=slide_order,
    )


